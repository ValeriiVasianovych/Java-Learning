public class Variables {
    public static void main(String[] args) {
        int one = 1;
        double two = 2.2;
        String three = "Three";
        boolean four = true;
        byte five = 5;
        long six = 61321231L;
        float seven = 7.23f;
        char eight = 'B';

        System.out.print("Variables: \n" + one + " " + two + " " + three + " " + four + " " + five + " " + six + " " + seven + " " + eight);
    }
}
